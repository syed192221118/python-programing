def string_operations():
    str1 = "Hello"
    str2 = "World"

    # Concatenation
    print(str1 + " " + str2)  # Output: "Hello World"

    # Reverse
    print(str1[::-1])  # Output: "olleH"

    # Slice
    print(str1[1:3])  # Output: "el"

    # Length
    print(len(str1))  # Output: 5

string_operations()
